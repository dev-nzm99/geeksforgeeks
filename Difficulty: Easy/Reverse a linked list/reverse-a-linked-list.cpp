class Solution {
  public:
    Node* reverseList(Node* head) {
        Node *curr,*prev,*nxt;
        curr = head;
        prev = nxt = NULL;
        while(curr){
            nxt = curr->next;
            curr->next = prev;
            prev = curr;
            curr = nxt;
        }
        return prev;
    }
};